public class OperadoresLogicos {
    
     public static void main(String[] args) {
     
        boolean operador1 = true;
        boolean operador2 = true;
        boolean operador3 = true;
        boolean operador4 = false;

        System.out.println(operador1 && operador2);
        System.out.println(operador1 || operador3);
        System.out.println(operador1);
        System.out.println(operador2 == operador3);
        System.out.println(operador2 == operador4);
        System.out.println(operador3 && operador4);

     }
     
}
